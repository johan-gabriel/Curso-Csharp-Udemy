 Os modificadores de acesso são palavras - chave usadas para especificar o acesso de um membro
(Campo, propriedade, método,etc) ou de um tipo (Classe, struct,etc).

public - Tipo que pode ser acessado por qualquer outro codigo;

private - Tipo pode ser acessado somente pela mesma class/struct;

internal - O tipo ou membro pode ser acessado por qualquer código no mesmo assembly, 
mas não de outro assembly

protected - O tipo ou membro pode ser acessado por qualquer código no assembly que possua uma classe derivada 
em outro assembly

file - Restringir o escopo e a visibilidade de um tipo de nivel superior ao arquivo no qual ele for declarado.
